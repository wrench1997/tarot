import random

# 大阿尔克那牌组
major_arcana = [
    "愚者", "魔术师", "女祭司", "女皇", "皇帝", "教皇", "恋人",
    "战车", "力量", "隐者", "命运之轮", "正义", "倒吊人", "死神",
    "节制", "恶魔", "塔", "星星", "月亮", "太阳", "审判", "世界"
]

# 牌的含义 (简化版)
card_meanings = {
    "愚者": "新的开始，冒险",
    "魔术师": "创造力，技能",
    "女祭司": "直觉，智慧",
    "女皇": "丰富，母性",
    "皇帝": "权威，领导力",
    "教皇": "传统，信仰",
    "恋人": "爱情，选择",
    "战车": "意志力，胜利",
    "力量": "勇气，内在力量",
    "隐者": "内省，指导",
    "命运之轮": "机会，变化",
    "正义": "平衡，诚实",
    "倒吊人": "牺牲，新视角",
    "死神": "结束，转变",
    "节制": "平衡，适度",
    "恶魔": "诱惑，束缚",
    "塔": "突变，启示",
    "星星": "希望，灵感",
    "月亮": "幻想，恐惧",
    "太阳": "快乐，成功",
    "审判": "重生，决定",
    "世界": "完成，圆满"
}

def draw_card():
    """抽一张牌并返回其名称和含义"""
    card = random.choice(major_arcana)
    meaning = card_meanings[card]
    return card, meaning

def tarot_reading():
    """进行一次三张牌的塔罗牌阅读"""
    print("欢迎来到塔罗牌阅读!")
    print("我们将为你抽取三张牌，分别代表过去、现在和未来。")
    
    input("准备好了吗? 按回车开始...")
    
    positions = ["过去", "现在", "未来"]
    
    for position in positions:
        card, meaning = draw_card()
        print(f"\n{position}的牌是: {card}")
        print(f"含义: {meaning}")
        input("按回车继续...")

    print("\n阅读结束。希望这次阅读能给你一些启发!")

# 运行程序
tarot_reading()